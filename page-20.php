    <h1>Пти-брабансон</h1>
    <p>Пти-брабансон – декоративная порода, относящаяся к группе малых бельгийских собак. Ласковые и любознательные, брабантские гриффоны сильно привязываются к своим хозяевам.</p>
    <h2>Основные моменты</h2>
    <p>Пти-брабансон отличается уравновешенным характером: он никогда не будет без повода бросаться на животных или прохожих.

        Брабантский гриффон любопытен, коммуникабелен и игрив. Питомец отлично ладит с маленькими детьми, положительно воспринимает розыгрыши и долго на хозяев не обижается.
        
        Четвероногие уроженцы Брабанта быстро привыкают к семьям, в которых живут, и эта привязанность навсегда. Длительная разлука с хозяевами способна ранить их нежную 
        психику, поэтому по возможности лучше брать гриффонов с собой в поездки.
        
        По мере взросления пти-брабансоны не теряют свою щенячью игривость, оставаясь в душе «детьми». Представители породы – очень общительные и обаятельные собаки. Они любят постоянно находиться в центре внимания.
        
        Уровень развития малого брабансона примерно соответствует интеллекту 3-летнего ребенка. Это означает, что дрессировке собака поддается хорошо и в будущем сохраняет привитые ей навыки.
        
        Брабантский гриффон уживается с любыми животными в доме.
        
        Пти-брабансон тонко чувствует настроение своего владельца и подстраивается под него, как истинный компаньон. Если хозяину грустно, то гриффон тоже расстроится, а если весело, то он с готовностью разделит мгновения радости.</p>
    <h2>Характеристики</h2>
    <p>Пти (малый) брабансон: Все окрасы, допустимые для гриффонов. Они имеют тёмную маску.
        Вес: от 3,5 до 6 кг</p>
