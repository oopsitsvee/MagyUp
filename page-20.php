    <h1>Пудель</h1>
    <p>Пудель – воплощение красоты и экстравагантной декоративности, сочетающихся с гармоничным сложением этой собаки, ее великолепным темпераментом, высоким интеллектом. Восхитительная внешность пуделя искрится артистичностью, экспрессией и шармом, а его доброта и жизнелюбие не знают границ. Эти замечательные собаки отличаются невероятной преданностью, которая распространяется на всех членов семьи, при этом пудели очень чувствительны к человеческому вниманию. Они веселые, озорные, любят дурачиться и всегда готовы принять участие в играх и забавах.</p>
    <h2>Основные моменты</h2>
    <p>Пудель – одна из самых добрых и послушных собак.

        Активен, способен выдерживать большие физические нагрузки.
        В городских условиях нуждается в длительных прогулках или активных играх.
        
        Все пудели, даже их самые мелкие разновидности, отличаются неприхотливостью, 
        обладают хорошим здоровьем и имеют статус долгожителей.
        
        Собаки абсолютно не агрессивны, напротив, наделены особой доброжелательностью к людям.
        
        Представители породы отлично ладят с детьми, становясь для них другом и партнером в играх.
        
        Пудели обладают густой пружинистой шерстью, универсальной для создания разнообразных причесок.
        Нуждаются в регулярном расчесывании и стрижке.    </p>
    <h2>Характеристики</h2>
    <p>Окрас. 
        Одноцветная шерсть: черная, белая, коричневая, серая, абрикосовая, красная.
        
        Рост. 
        Миниатюрный пудель от 28 до 35 см.</p>
